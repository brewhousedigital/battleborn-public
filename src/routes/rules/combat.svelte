<h2>Combat</h2>

<h3>Step 1: Rolling for Initiative</h3>
<p><i style="font-size:12px;">your favorite words</i></p>
<p>When players enter combat, they each roll 1d20 to decide who goes first. If a character initiates an attack then they will go first</p>



<h3>Step 2: Choosing your action</h3>
<p>Each character during combat has 2 actions that they can take each round:</p>
<ul>
    <li>Attacking</li>
    <li>Moving</li>
    <li>Charging magic</li>
    <li>Using spells</li>
    <li>Using potions</li>
    <li>Changing armor/weapons</li>
    <li>Getting up after being knocked down</li>
</ul>





<h3>Rules of Attacking</h3>

<h4>Rolling to Hit</h4>
<p>When a player attempts to attack someone, they must roll 1d20 to hit. You must get above your opponent's dexterity or wisdom to hit the target.</p>
<p>If a character rolls a 20, the attack cannot be dodged by the target.</p>
<p>If a character rolls a 1, the attack critically misses and may cause damage to the player or those around you. This is especially dangerous for ranged attacks.</p>


<h4>Melee</h4>
<p>A character with a melee weapon must be in front of their target when attacking. Roll weapon die, then add your strength and any other bonuses. Subtract this number by the target's physical armor and bonuses for the final damage.</p>




<h4>Ranged</h4>
<p>A character with a ranged weapon can attack a target that is 10 squares away and the target must be in the line of sight. Each additional square after will decrease your attack roll by 1. To attack, roll the weapon die, then add your strength and any other bonuses. Subtract this number by the target's physical armor and bonuses for the final damage.</p>






<h4>Spells</h4>
<p>Spells can be either touch spells or distance spells. Distance spells can only affect a target that is 10 squares away. Roll spell die, then add your intelligence and any other bonuses. Subtract this number by the target's magic armor and bonuses.</p>






<h4>Non-Combat Spells</h4>
<p>Please refer to Skill and Spell Checks for information on non-combat spells.</p>






<h3>Armor and Spell Resist</h3>
<p>When a player is attacked, they can suffer physical or magic damage.</p>

<p>Armor can provide a wide range of benefits including:</p>
<ul>
    <li>Reducing physical or magical damage</li>
    <li>Enhancing your own magic abilities</li>
    <li>Increasing the damage of your own attacks</li>
    <li>Disguising yourself</li>
    <li>Resisting different schools of magic</li>
</ul>
