<h2>Leveling Up</h2>
<p>To level up your character, first click on the "Edit Stats" button in the top right corner of your character sheet</p>

<ol>
    <li>In here, increase your level number by one (or however many levels you gained).</li>
    <li>Next, you can calculate your health increase by rolling a 1d4 and adding your constitution attribute. Then increase your maximum health by this number.</li>
    <li>The last step is to add two points to the attributes of your choosing.</li>
    <li>Don't forget to save!</li>
</ol>
