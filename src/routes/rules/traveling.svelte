<h2>Travel</h2>

<p>When your party travels across a large swath of land, you'll need rations to keep you from going hungry. You'll need 1 ration per person in your party for each overland square you travel through. If you dont have enough gold to buy rations, you'll probably need to do some quests in town first.</p>

<p>Also when traveling, there are small chances of random encounters, especially on roads not controlled by the country's armies. A chance to find an abandoned merchant cart, or stumble upon a trap set by the bandits who live in that area.</p>
