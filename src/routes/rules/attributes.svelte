<h3>Attributes</h3>

<h2>What Are Attributes?</h2>

<p class="mb-5 italic">The six forces that created the universe...</p>

<h3 class="fw-bold">Strength / Str</h3>

<p>Strength measures physical power. This is important for melee and ranged weapons used in combat. Examples of using strength: melee attacks, shooting a bow, blocking, climbing, swimming, grappling, squeezing someone's hand when you first meet them, and bashing through doors.</p>

<ul>
    <li>One handed weapons (main hand) use your strength + weapon die.</li>
    <li>One handed weapons (off hand) use the weapon die.</li>
    <li>Two handed weapons use your strength + weapon die.</li>
    <li>Bows use your strength + weapon die. *Ranged weapons have a limited range of attack.</li>
</ul>

<p class="italic">TL;DR: Physical Damage</p>

<div class="clearfix mb-5"></div>

<h3 class="fw-bold">Dexterity / Dex</h3>

<p>Dexterity measures agility, reflexes, and balance. Dexterity is vital for characters wanting to avoid taking damage. Examples of using dexterity: dodging attacks, sneaking past someone, pickpocketing, and trying to carefully remove a necklace off a pinned zombie lord while avoiding it's ferocious bite.</p>

<ul>
    <li>Increases likelihood of landing physical attacks. The higher your dexterity, the higher chance to land a successful attack on your opponents.</li>
    <li>Dodging melee and ranged physical attacks from your enemies. The higher your dexterity, the higher chance to dodge.</li>
    <li>Moving undetected and pickpocketing.</li>
    <li>Riding and controlling a horse or other riding animal.</li>
</ul>

<p class="italic">TL;DR: Physical Defense</p>

<div class="clearfix mb-5"></div>

<h3 class="fw-bold">Constitution / Con</h3>

<p>Constitution represents your characters overall health and wellbeing. Examples of using constitution: praying to a deity, holding firm to your psyche while you watch a horrific event unfold before your very eyes, putting all your points into constitution early so your health becomes exponentially large at the end of the game.</p>

<ul>
    <li>Each time you level up, your constitution is added to your health bonus.</li>
    <li>Resisting poisons and diseases.</li>
    <li>Deity Checks.</li>
</ul>

<p class="italic">TL;DR: More Health</p>

<div class="clearfix mb-5"></div>

<h3 class="fw-bold">Intelligence / Int</h3>

<p>Intelligence represents how well your character learns and reasons. This is vital for mages and seers. It is primarily used as your attack power for magic. Examples of using intelligence: attacking an enemy with a spell, deciphering clues from a dungeon, remembering someone you saw in a crowd eight sessions ago, and having a <i>big</i> brain.</p>

<ul>
    <li>Controls spellcasting and spell damage. A higher intelligence lets you cast stronger spells.</li>
    <li>Ability to appraise items.</li>
    <li>Ability to craft new items from scraps or recycling current items.</li>
</ul>

<p class="italic">TL;DR: Spell Damage</p>

<div class="clearfix mb-5"></div>

<h3 class="fw-bold">Wisdom / Wis</h3>

<p>Wisdom describes a character's willpower, common sense, awareness, and intuition. Wisdom is handy for resisting the spells of lesser wizards. Examples of using wisdom: resisting damage from a novice mage, reflecting that same damage back to the novice mage, knowing when that same mage is going to get killed by his own spell, and deciding to spare the novice mage in return for his undying loyalty to you as his new wizard master.</p>

<ul>
    <li>Resisting magical attacks from your enemies. The higher your wisdom, the higher chance to resist.</li>
    <li>Affects physical healing such as bandages and surgery.</li>
    <li>Sensing when bad things are about to happen and reacting to traps.</li>
</ul>

<p class="italic">TL;DR: Spell Defense</p>

<div class="clearfix mb-5"></div>

<h3 class="fw-bold">Charisma / Cha</h3>

<p>Charisma measures a character's personality and appearance. The larger your charisma, the larger your... personality. You can talk the talk without having to walk the walk and others will follow. Examples of using charisma: convincing two arguing people to come to an agreement, get out of jail without having to serve time, and convince people that you really are <a href="/rules/mr-bearington">more than a roaring bear</a>.</p>

<ul>
    <li>Affects bluffing, intimidation, and influencing others.</li>
    <li>Affects disguises, performance, and diplomacy.</li>
    <li>Allows you to control animals and use them as companions.</li>
</ul>

<p class="italic">TL;DR: Communication Skills</p>
