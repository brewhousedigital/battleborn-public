<h3>Leveling Professions</h3>
<p>Professions start at the Apprentice level and can level up by working hard at your craft or by paying a trainer to
    teach you the ways.</p>
<p>Having a profession gives you a +3 bonus to your roll, and increases an additional +3 with each increased rank.</p>
<ol>
    <li>Apprentice, +3</li>
    <li>Journeyman, +6</li>
    <li>Expert, +9</li>
    <li>Artisan, +12</li>
    <li>Master, +15</li>
</ol>























<h3>Companions</h3>
<div class='navcompanion' style=''>
    <p>Rangers, Survivalists, and Spirit Bond Druids can have companion animals that follow them throughout the game. These companions are treated as a second character for the player and each have their own actions.</p>
    <p>Companions can move, attack, or follow other commands from the player as long as they are not complex (i.e. go retrieve a potion from Cedric's house is too complex of a command for a companion to follow). Each companion rolls a 1d6 + their damage bonus when they attack.</p>
    <p>There are three categories of companions:</p>

    <h3 class="fw-bold"><i class="fal fa-dragon me-3"></i> Ferocious</h3>
    <p>Ferocious animals typically have high strength, average speed, average defense, and average health. They are most useful for dealing damage as long as they can stay out of the way of enemy attacks. These companions excel at taking out smaller, easier enemies while the rest of the party focuses on the more powerful foes.</p>

    <h3 class="fw-bold"><i class="fal fa-monkey me-3"></i> Cunning</h3>
    <p>Cunning animals are very speedy and have the highest movement points out of the three groups. Their health is relatively low, their armor is usually nonexistent, and their attacking power is average. These companions are most useful for scouting and providing support rather than being in the middle of a battle.</p>

    <h3 class="fw-bold"><i class="fal fa-badger-honey me-3"></i> Tenacious</h3>
    <p>Tenacious animals are hardy and can provide the most defense to their animal handler. These companions have high armor and can withstand many blows before showing fatigue. Though their strength and speed are low, their health makes up for any shortcomings. These animals are great for making diversions to catch your opponents by surprise.</p>

</div>
