    <h3>Race against the clock</h3>

    <p>The players need to talk to the right people to trigger the right event to get the barkeep to help. Any time the players deviate from this, leave, drink too much, gamble too much, or go to sleep, will end up dying and restarting.</p>

    <h3>Act 1</h3>

    <p>Players must refuse the wine from the Barkeep and then approach the human. He will see that you haven't been drinking and will ask if you feel the 'weird air' around this place. He will mention that he heard a rumor that something evil was going down in this place. He will tell the players hes going to talk to the Barkeep and to go chat with the others until he gets back.</p>

    <h3>Act 2</h3>

    <p>The players must then talk to the Elf and ignore his anti-dwarf notions, and ask if he feels if theres anything wrong here, where he'll say yes, and that hes noticed theres magic in the wine but didn't care enough to stop drinking it.</p>

    <p>Once theyre done talking to the elf, the human will walk back to his table</p>

    <h3>Act 3</h3>

    <p>If the players approach him and ask what happened, he'll say nothing wrong is going on take a drink of his wine. He'll say he just wants to enjoy the rest of the night</p>

    <h3>Act 4</h3>

    <p>Players can approach the barkeeper and he'll explain that the human had a misunderstanding about where he was. Then fill up some cups of wine for the group and tell them that fresh baked bread is about to be ready. The players have to intimidate the barkeep.</p>

    <p>If intimidation is successful, the barkeep will burst into tears and explain that vampires were on their way to kill them and that he had no choice.</p>

    <p>Players must pass a charima check to convince the barkeep that they will protect him from the vampires</p>

    <p>If successful, the barkeep will run to the front door and add an enchantment rune on it, and activate other runes around the bar. He then pulls out a small scrying orb and pours his magic into it. He said he is summoning the vampire hunting guild from a nearby town and that hopefully they'll be here.</p>

    <h3>Act 5</h3>

    <p>Now the players can start boarding up the tavern, rolling strength checks to lift up tables. There are three tables total.</p>

    <p>Once completed, they will start hearing yelling and a loud voice booming to let them in. The voice says the name of the barkeep and that he will torture him for eternity if he doesn't open up. The barkeep looks as the players and demand that they kill him if the vampires get in. The players have to say yes. If they dont, the barkeep will turn on them and open the door to the vampires.</p>

    <p>Soon they'll see smoke and fire all around. They are going to be burned inside the tavern.</p>

